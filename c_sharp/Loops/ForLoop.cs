// This program demonstrates the use of a 'for' loop, ideal for iterating a known number of times.
using System;

class Program
{
    static void Main()
    {
        // For loop example: Print numbers from 1 to 5
        for (int i = 1; i <= 5; i++)
        {
            Console.WriteLine($"Number: {i}");
        }
    }
}
