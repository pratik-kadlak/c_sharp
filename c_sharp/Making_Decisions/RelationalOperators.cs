// Program demonstrating relational operators in C#
using System;

class RelationalOperators
{
    static void Main()
    {
        int x = 10, y = 20;
        Console.WriteLine("x == y: " + (x == y));
        Console.WriteLine("x != y: " + (x != y));
        Console.WriteLine("x < y: " + (x < y));
        Console.WriteLine("x > y: " + (x > y));
        Console.WriteLine("x <= y: " + (x <= y));
        Console.WriteLine("x >= y: " + (x >= y));
    }
}
